using System;

class DeclaracaoVariaveis
{
	static void Main(){
		/* 
		Declaração de Variável
		*/
		int a;
		// Inicialização da Variável
		a = 22;
		Console.WriteLine(a);
		// Reatribuição de Valores na Variável
		a = 56;
		Console.WriteLine(a);
		
		// Outras formas de declaração de Variável
		
		// Declarando e Inicializando a Variável
		int b = 925;
		Console.WriteLine(b);
		
		int y = 10, x = 20;
		int z = y + x;
		Console.WriteLine(z);
		
	}
}